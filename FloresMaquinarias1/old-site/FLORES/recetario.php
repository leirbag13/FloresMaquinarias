		  <P>Poner en la amasadora el agua,  la sal, el azúcar y el extracto de malta, mezclar todo sin poner en marcha,  desgranar la levadura y volcar la harina con le antimoho. Poner en marcha la  amasadora, el tiempo de amasado dependerá según la maquina, si es rápida o lenta.</p>
