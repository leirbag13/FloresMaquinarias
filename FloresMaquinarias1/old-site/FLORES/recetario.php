          <h3>Glosario</h3>
          <p>Para comprender en este  recetario el lenguaje técnico, es necesario, como en todo oficio, explicar los  términos que usa el mundo de la panificación, y la denominación de sus  maquinarias.</p>
		  <p><u><strong>Amasadora de alta velocidad:</strong></u> Por su  amasado rápido casi no necesita el sobado de las masas, ya que las deja muy  sedosas.</p>
		  <p><u><strong>Sobadora:</strong></u> Maquina que  estira y soba las masas al paso de estas a través de dos cilindros.</p>
		  <p><u><strong>Batidora:</strong></u> Sirve para  la elaboración de bizcochuelos, merengues, etc., montando los preparados de  huevo, especialmente.</p>
		  <p><u><strong>Torno:</strong></u> Mesa en la  que trabaja el panadero o el pastelero.</p>
		  <p><u><strong>Brilla o palote:</strong></u> Cilindro de  madera para estirar o enrollar la masa a mano dándole el espesor deseado.</p>
		  <p><u><strong>Amasijo:</strong></u> Masa trabajada  con todos los ingredientes.</p>
		  <p><u><strong>Bastones:</strong></u> Trozos de  masa cruda que se forman en forma de bastón para poder trabajar mas  cómodamente.</p>
		  <p><u><strong>Bollar:</strong></u> Darle a la  masa forma esférica.</p>
		  <p><u><strong>Empastar:</strong></u> Mezclar  harina con materia grasa.</p>
		  <p><u><strong>Estibar:</strong></u> Colocar  piezas crudas prolijamente en latas.</p>
		  <p><u><strong>Granza:</strong></u> Masa dura,  disgregada, no unificada.</p>
		  <p><u><strong>Levadura previa:</strong></u> Parte  proporcional del amasijo a realizar que incluye levadura. Una vez amasado se le  da descanso para su fermentación y luego se lo incorpora con el resto de los  ingredientes formando el amasijo total. Este sistema da una mayor fuerza a la  masa.</p>
		  <p><u><strong>Liga:</strong></u> Es la tensión o la  elasticidad de la masa.</p>
		  <p><u><strong>Picar:</strong></u> Esta operación  consiste en perforar la masa de bizcochos, galleteria o criollos, previamente  estirada sobre el torno evitando así la formación de globos al cocinarse.</p>
		  <p><u><strong>Puntear:</strong></u> Dejar que las piezas  elaboradas con levadura tomen el volumen necesario.</p>
		  <p><u><strong>Tamizar o cernir:</strong></u> Operación  que consiste en pasar las harinas y/o féculas por el tamiz o cedazo, tanto para  sacar las impurezas o grumos de las mismas como también para alveolar airear y  facilitar las mezclas.</p>
		  <h3>PARA TENER EN CUENTA:</h3>
		  <p><u><strong>Moho en la panificación:</strong></u> son  hongos (plantas microscópicas) que se desarrollan principalmente en verano,  manchando y formando capas sobre la superficie de los productos terminados, mas  aun en los de mayor contenido de humedad y cuya venta o consumo sea inmediata.  Ejemplo: pan de viena, pizzas, hamburguesa, budín ingles, pionono, etc.<br />
              <u><strong>Como prevenir:</strong></u> agregar a los  amasijos 3 gr. de antimoho por kg. de harina, cocinándolos bien; desmoldar los  productos, colocarlos sobre rejillas, ventilarlos y envasarlos una vez  evaporada totalmente la humedad.</p>
		  <p><u><strong>Filamentacion:</strong></u> es una  infección que se manifiesta con facilidad en verano; es altamente contagiosa y  la produce un microorganismo (basilius mesentericus) que suele hallarse en el  medio ambiente, en las harinas viejas y los desperdicios de las panaderías<br />
		    Se presenta en el centro de las piezas horneadas y aparece como una mancha  amarillenta con fuerte olor, y a medida que pasan los días se agrava, volviendo  las piezas crudas en el centro con olor putrefacto y forma pegajosa formando  filamentos.<br />
            <u><strong>Como prevenir:</strong></u> es necesario  mantener la cuadra, el deposito y todas las instalaciones en óptimas  condiciones de higiene.<br />
            <u><strong>Como combatir</strong>:</u> apenas  detectado el bacilo retirar toda la producción infectada y limpiar los lugares  de trabajo, utensilios, maquinas, pisos y paredes con una solución de 8 litros  de agua hirviendo y 2 litros de vinagre blanco, repitiendo esta limpieza por varios  días.</p>
		  <p><u><strong>Fermentación:</strong></u> es el proceso  de desarrollo de las masas elaboradas con levadura ya sea prensada o seca,  produciéndose (por acción de las enzimas en la harina) la transformación del  almidón en azúcares.<br />
		    La levadura que se elabora a partir de cepas denominadas saccha romyces  carelisias, actúa sobre los azúcares y los transforma en gas (anhídrido  carbónico) y en alcohol etílico o etanol provocando el crecimiento de la masa.<br />
		    La temperatura ideal de la masa es 26° C. Para su posterior desarrollo, de  allí, las piezas preparadas y puestas a fermentar en la estufa a 35° C, con un  85% a 95% de humedad es lo ideal para su leudado o aumento de volumen, y  posterior cocción. Hay que tener en cuenta que si pasamos de 45° C la estufa,  la levadura comenzara a deteriorarse, por lo tanto dejara de trabajar y morirá  produciendo la perdida de las piezas.</p>
